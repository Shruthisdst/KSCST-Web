<h1>Scientists Students Interaction Programme (SSIP)</h1>
<h2>&nbsp;</h2>
<h3>Background</h3>
<p>In order to improve the quality of education in the State especially in rural areas, it is felt necessary to introduce some effective programme in schools and colleges. This would help in creating awareness in the students and improve their career to undertake science as profession. Some of the programmes have been already under implementation by Government of Karnataka. Inspired by the successful programmes like "Mahithi Sindhu" and the extension lecture programme of Indian Institute of Science, Karnataka State Council for Science and Technology has taken a decision to conduct programmes of interaction of students with scientists in the schools and colleges throughout the state. This programme is called the Scientists Students interaction Programme (SSIP).</p>
<p>This programme was experimented on one occasion and it was successful. The students were excited to be with the scientists and participated actively by posing a large number of questions. It is felt that this is one of the way of popularising science and technology in many schools in the State as well as ignite the curiosity of Students. This will also motivate the students to go in for basic science education.</p>
<h3>Objectives of SSIP</h3>
<ul>
	<li>To Promote active involvement of students and teachers in scientific activity.</li>
	<li>To encourage students to ask questions and doubts in many areas of science and promote their effective participation.</li>
	<li>To enable students to know about the latest developments in the fields of science and technology.</li>
</ul>
<h3>Methodology</h3>
<p>The programme will have students as participants from several schools/colleges and high schools and pre-university colleges. Major participants will be from Mahithi Sindhu schools. Several resource persons from different disciplines will form the panel. They will be chosen from research institutions like Indian Institute of Science, national laboratories, leading educational institutions etc. In order to break the ice, there may be short presentation by experts. After this, interaction will begin with questions from students. During this discussion students can ask questions and clarify their doubts.</p>
<p>The programme will be bilingual in both Kannada and English. The entire duration will be 3 - 4 hours. Karnataka State Council for Science and Technology shall organize this programme. Directorate of State Education Research and Training takes the responsibility for selecting and bringing the students for the programme and the nodal agency to organize this programme. They will be assisted by the District Institution for Education at the district level.</p>
<h3>Programmes conducted during 2004 -2005</h3>
<p>The council has so far conducted 16 SSIP programmes in different districts of Karnataka. During the year 2004-2005, five programmes were conducted and the details are as below:</p>
<p><strong>The Scientists Students Interaction Programme was organized by the Council in Sullia, Dakshina Kannada district, on 6th August 2004</strong>. The programme was attended by over 200 students. Shri K. Venkatramana Gowda, President, Kurungi Venkatramana Gowda College of Engineering and Technology, Sullia inaugurated and presided over the function. Prof. D. K. Subramanian, Secretary, Karnataka State Council for Science and Technology was the chief guest.</p>
<p><strong>The Scientists Students Interaction Programme was organized by the Council was involving Prof. C. N. R. Rao and the high school students organized on 24th January 2005</strong> was attended by nearly 1000 students belonging to 25 high schools in Bangalore. The programme was also attended by the Honourable Minister for Science and Technology Sri Basavaraj Horatti. Prof. M. K. Surappa welcomed the gathering followed by a lecture by Prof. C. N. R. Rao on "Science Learning". Later Prof. Indumati Rao of Jawaharlal Nehru Centre for Advanced Science and Research demonstrated CD-ROM developed by the centre on learning science on various subjected. The programme was also attended by Prof. Pervez Amirali Hoodbhoy Department of Physics, Quaid-e-Azam University, Islamabad, Pakistan, who has been awarded UNESCO Kalinga Prize for Popularization of Science 2003. The Directorate of Science Education Research and Training also supported the programme.</p>
<p><strong>Scientists and Students Interaction Programme at Forestry College, Ponnampet:</strong> An interaction programme was organized by the council on 25th February 2005 at College of Forestry, Ponnampet, Coorg district. The programme was attended by nearly 150 students of Mahithi Sindhu schools of Coorg district. The programme was inaugurated by Shri K. N. Chengappa, Chairman, Kodagu Model Forest Trust and presided by Prof. C. S. P. Patil, Director of Instruction, College of Forestry, Ponnampet. Dr. S. G. Sreekanteswara Swamy spoke about the objectives of the SSIP programme and Mrs. Pratima Rao spoke about Mahithi Sindhu Schools in Karnataka and their importance. The scientists attended were Dr. Prasad Bothla, SSO, Department of Physics, Indian Institute of Science, Bangalore, Dr. P. Nagaraja, Reader, Dept of Chemistry, Mysore University, Mysore; Dr. S. V Narasimhan, Physician, Virajpet, Smt. Ramamani, Shri Anand, Shri Natarajan, from Coorg Institute of Technology, Ponnampet.</p>
