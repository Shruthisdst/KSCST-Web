<p><strong>Popular Science Lectures at Indian Institute of Science:</strong> A series of 3 lectures was organized on 28th February 2005 inviting about 50 students from high schools of Bangalore in Choksi hall, Indian Institute of Science, Bangalore. The programme was inaugurated by Prof. K. J. Rao and Prof. M. K. Surappa, Secretary, KSCST, presided over the function. Prof. K. J. Rao spoke about the "A hole in the Ozone layer" and explained the environmental disaster eminating from it. He also explained the reasons for such a phenomenon. Prof. Surappa spoke about the importance of propagating scientific temper among students and the general public. Later Prof. H. L. Bhat of Department of Physics spoke about Photo-Electric effect, one of the important inventions in the beginning of the last century. Finally Prof. Uday Bhaskar of Raman Research Institute spoke about the invisible universe and radio astronomy.</p>
<p><strong>Programme on Observational Astronomy:</strong> A programme was held at Pilikula, Sullia taluk, Dakshina Kannada district, on observational astronomy.</p>
<p><strong>Prof. C. N. R. Rao interacted with nearly 500 high school students in and around Mysore on 5th November 2005 in Mysore.</strong> Later Prof. Indumati Rao of Jawaharlal Nehru Centre for Advanced Science and Research demonstrated CD-ROM developed by the centre on learning science on various subjects. Central Food Technological Research Institute, The Directorate of Science Education Research and Training, and Bharat Gyan Vigyan Samithi also supported the programme.</p>
<p><strong>Several questions that were posted by the students in various programmes conducted have been gathered and is being published as a book in kannada, the local language, and will be distributed to various schools of karnataka.</strong></p>
<h3>Recent Programmes</h3>
<p>The Scientists Students Interaction Programme was organized by the Council involving Prof. C. N. R. Rao, FRS, National Research Professor, Jawaharlal Nehru Council for Advanced Scientific Research and the high school students in Dharwar on Friday, September 15, 2006. The venue of the programme was at Srujana Auditorium, Karnataka Science College Campus, Dharwad. The programme started at 10.30 AM. Prof. C. N. R. Rao delivered a lecture on <strong>"Science Learning"</strong> in Kannada. Later Prof. Indumati Rao of Jawaharlal Nehru Centre for Advanced Science and Research demonstrate CD-ROM developed by the centre on learning science on various subjects. Hon'ble Minister for Primary and Secondary Education Sri. Basavaraj Horatti, Incharge Vice Chancellor of Karnataka University, Dharwad Dr. Murigeppa and others were present during the function. Karnataka University, Dharwad and The Directorate of Science Education Research and Training supported this programme.</p>
