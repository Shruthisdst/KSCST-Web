<h3>Introduction</h3>
<p>The Council initiated the Karnataka Natural Resources Data Management System (NRDMS) program in 1992 and since then established District NRDMS Centers in all the State with active support and funding from Department of Science &amp; Technology (DST) Government of India and Government of Karnataka. NRDMS program is a multi-disciplinary and multi-institutional program of the Council aimed at developing methodologies for building and promoting the use of spatial data management and analysis technologies in local area planning. The initial approach of planning based on macro level assessment of resources did not yield the desired results. The introduction of the concept of spatial planning to micro-levels by the Planning commission paved the way for evolution of NRDMS program. The vision of the NRDMS program during its inception was to provide S&amp;T inputs for operationalising the concept of decentralized planning of the country by developing computer compatible spatial databases on natural resources, socio and agro-economic parameters to further the concept of area specific decentralized planning.</p>
<p>Resource knowledge and resource inventory is crucial to spatial planning. At the core of this concept lies an integrated approach to planning in contrast to the sectoral method. This requires a detailed knowledge of the interrelations and interdependencies between various sectors to resolve often-conflicting demands. This leads to a requirement for appropriate data management and analyzing tools and techniques and a large matrix of sectoral data, in digital format, on natural resources, demography, socio-economy etc. and integrating them to generate appropriate information/applications required for plan preparation.</p>
<p>The district NRDMS centers since inception have created an exhaustive database of natural resources, socio-economic parameters, infrastructure etc. The NRDMS centres are providing analyzed/value added information to planners and administrators besides developing applications using geo-spatial technologies suitable for local level planning. The NRDMS centers are providing need-based information to users such as elected bodies, district administration and planners through maps, charts, tables, and reports etc., on spatial and non-spatial data.</p>
<h3>Vision</h3>
<p>Enabling people, communities and institutions of local-self governance with requisite databases and S &amp; T tools for informed participation in local self-governance.</p>
<p>The corresponding goals of the Programme to realize the above vision could be the following:</p>
<p>To strengthen the S&amp;T inputs in developing tools and techniques for integrated resource and infrastructure capacity building at various levels for planning and implementation in a spatio-temporal context in a multi-level framework below the district.</p>
<h3>Genesis of NRDMS</h3>
<p>The balanced regional development and effective implementation of the basic needs of our society depends on the planning process which was initially the sectoral approach confined to the central level. This accentuated the inter and intra-regional disparities in the development and hence there was necessity to decentralize the planning process to smaller area units. During the VIII five-year plan a special emphasis was rightly given to the concept of decentralized planning. While adopting the seventy third and seventy fourth constitutional amendment acts (1992), a set of specific items covering various sectors viz., land, water, agro-economy etc., were identified. Concerned agencies were required to prepare and implement integrated development schemes. This growing urge for decentralizing the process and practice of planning prompted to take a fresh look at methods of data management.</p>
<p>In line with the above policy, the Natural Resources Data Management System (NRDMS) project was launched by the Department of Science and Technology, Government of India.</p>
<p>The basic imperative behind evolving NRDMS are:</p>
<ol>
	<li>Large diversity of spatial data.</li>
	<li>Many types of data are used for more than one purpose by more than one user.</li>
	<li>Utility of data set may be greatly enhanced by merging or linking it with other data sets.</li>
	<li>Reliability / consistency can be enhanced by correlating different parameters.</li>
	<li>Many organisations are involved in data collection and updating.</li>
	<li>Need for standardisation / compatibility of procedures of data collection and adoption.</li>
	<li>Need to improve the utilisation of data for decision making.</li>
</ol>
<p>The overall objective of the NRDMS is to increase the efficiency of utilization of resources through the application of scientific management principles and Geographical Information System [GIS] technology.</p>
