<h4>First Decade (1992-2002)</h4>
<p>At the local level, necessary institutional changes were brought about during this period to ensure people's participation in the planning process. As per the provisions of the 73rd and 74th constitutional amendments, different State Governments constituted a three tier system of local bodies at district (Zilla Panchayath), block (Panchayath Samiti) and village (Gram Sabha) levels at rural areas and Municipalities (Nagarpalikas) at urban areas. Mandates of such local level bodies are to collect revenues, draw up and implement integrated development schemes. The VIII 5 Year- Plan (1990-95) of Planning Commission suggested that the developmental plans should be drawn up at the local level (district and below) taking into account the physical and human endowments of the area, felt needs of the people and funds available .Entry of GIS in India during late 80's and launching of Indian Remote Sensing Satellite during 1987 guided the evolution of the NRDMS programme during this decade. The programme was looked upon as a major initiative for introduction of Geospatial technologies and tools in the local level planning of the State.</p>
<p>In this context, the goals were focused towards :</p>
<ol>
	<li>Development of spatial data management technologies for integrated rural development planning and</li>
	<li>Demonstration of utility of spatial data tools in local level decision making. Adoption of Geographical Information System (GIS) as the core of NRDMS methodology for locale specific problem solving was made explicit. Forging linkages with users at different levels was introduced as one of the objectives for dissemination of NRDMS methodology.</li>
</ol>
<h4>Major Outputs of the NRDMS programme in the first decade (1992- 2002)</h4>
<ol>
	<li>Improved procedures for data collection, collation and processing at local level developed.</li>
	<li>Indigenous GIS package GRAM extensively used for customization and application development.</li>
	<li>Spatial Decision Support Systems for health, education, watershed, tourism and infrastructure developed.</li>
	<li>User-friendly interfaces developed for language conversion, visualization and spatial applications.</li>
	<li>Spatial district resource profiles and user specified applications developed and demonstrated at district level in selected sectors of infrastructure location/management, health, natural resource management, election management etc.</li>
	<li>Seventeen district NRDMS centre established.</li>
	<li>Availability of the Core Expertise in Spatial Data Management in Panchayath Raj Institutions/Districts / State.</li>
</ol>
<h4>Major Applications</h4>
<ul>
	<li><strong>Location/allocation of health facilities :</strong> The purpose of this application is to locate existing health facilities and indicate up-gradation / new location of medical facilities as per the health department norms.</li>
