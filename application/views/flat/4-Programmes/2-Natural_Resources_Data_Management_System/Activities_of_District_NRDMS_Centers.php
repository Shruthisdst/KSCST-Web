<h2>Natural Resources Data Management System - Karnataka Project</h2>
<h3>HIGHLIGHTS OF WORK CARRIEDOUT BY DISTRICT NRDMS CENTRES</h3>

<h4>Principal Investigator</h4>
<p><a href="#">Mr. H. Hemanth Kumar</a></p>
<h4>Background</h4>
<p>Under a joint project of Natural Resources Data Management System (NRDMS) of Department of Science &amp; Technology, Government of India, Government of Karnataka, Karnataka State Council for Science &amp; Technology (KSCST), Bangalore has developed district level GIS databases to support the process of Local Level Planning at the levels of district, taluk and village. Over the past two decades all the thirty districts of Karnataka State have been covered under NRDMS program. Each District Database contains spatial and attribute data sets useful in local level planning processes in the sectors of Natural Resources, Demography, Agro-economy, Socio-economy and Infrastructure facilities. Respective Zilla Panchayaths and Line Departments have been depending upon the databases for meeting their data requirements. Recently, at the behest of CEO/CPOs of the Zilla Panchayats, the Council has initiated Resource, Infrastructure and Habitation mapping using Geospatial Technologies for Gram Panchayaths / ULB's in Karnataka in few selected districts for informed decision-making.</p>
