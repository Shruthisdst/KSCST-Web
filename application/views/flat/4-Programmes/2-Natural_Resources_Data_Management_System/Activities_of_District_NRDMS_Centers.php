	<li class="gap-above-small">Monitoring, evaluation, Learning and Documentation of IWMP program of watershed Development department using Geospatial analysis in Belgaum division.</li>
	<li class="gap-above-small">District centres have been continuously supporting the NREGA, BRGF, Panchatantra, MPIC, Total Sanitation program, Lokamitra package, isaster Management, Nirmala Gram Program, water supply project under Jal Nirmal program ZP website, KVK program etc at district level.</li>
	<li class="gap-above-small">Preparation of infrastructure and amenities of Zilla/Grama Panchayaths using GPS and Satellite imageries.</li>
	<li class="gap-above-small">NRDMS centres are providing analysed spatial information to support district planning activities mainly in the areas of education, health, infrastructure, watersheds based on local specific problems etc.</li>
	<li class="gap-above-small">Technical support to district planning in resource mapping using high resolution satellite imageries, GPS etc., specifically in Chitradurga, Madikeri, Hassan, Uttara Kannada and Mandya districts.</li>
	<li class="gap-above-small">Provided technical reports to Divisional Commissioner, Gulbarga regarding status of teachers/students ratio in all the schools.</li>
	<li class="gap-above-small">Supporting Shimoga district AIDS Control Organisation by providing analysed information using Geospatial Technologies. The work was very much appreciated by CEO, Shimoga.</li>
	<li class="gap-above-small">Development and implementation of web GIS for RDPR under the Government of India supported biogas program using geospatial technologies.</li>
	<li class="gap-above-small">Stone crusher mapping with recent relaxed criteria for identifying areas suitable for stone crushers.</li>
</ul>
<h4>Activities of district NRDMS centre activities during 2009-10</h4>
<ul>
	<li class="gap-above-small">District level user interaction workshops have been conducted on "Web based Geographic Information System" to create awareness among end user in the utility of geospatial database through Internet. During these workshops the utility of the GeoPortal was demonstrated to the end users for planning and management of resources. The Council conducted district level workshops in twenty four districts of the State. The activities of the NRDMS program and the web based GeoPortal evoked good response among the district officials. Several requests were received during the workshop to take up activities utilizing geospatial technologies to developmental programs and also to provide a detailed technical / project reports to plan the resources and infrastructure.</li>
	<li class="gap-above-small">The NRDMS Centres have prepared Technical reports containing basic information and arrangements made in Bye-Elections - 2005 as well as Gram Panchayath elections held during 2009-10. The work involved generating Legislative Assembly / Gram Panchayath constituency wise location and jurisdiction of polling stations with numbers and type of polling booths along with route maps to carry polling personnel and materials as required by election authorities, Election Commission and Observers.</li>
	<li class="gap-above-small">Preparation of Status report on rainfall, crops, ground water table in comparison with previous years and drinking water facilities.</li>
	<li class="gap-above-small">Detailed project report as per the requirement of Sericulture department on mulberry cultivation and projected area of its cultivation by the year 2020.</li>
	<li class="gap-above-small">Prepared Project Implementation Programme report under Total Sanitation Campaign project of Government of India. The report included necessary inputs for TSC program by incorporating drinking water and sanitation facilities of villages selected under this programme.</li>
	<li class="gap-above-small">District centres have been continuously supporting both central and state supported development programs like NREGA, Backward Region Grant Fund (BRGF), Suvarna Gramodaya program, Integrated Wastelands Development Programme (IWDP), National Watershed Development Programme in Rain fed Area (NWDPRA) and NABARD assisted watershed program etc.</li>
	<li class="gap-above-small">Crime mapping of several City Municipalities for based on intensity of crime, availability of police stations etc.</li>
	<li class="gap-above-small">Development of GIS based district rural road plan for link roads. In several districts the habitations are wide spread, and these habitations are connected to major roads by roads called link roads. This study indicated the current status of various categories of roads, their connectivity to habitations and other related matters regarding the road network of especially coastal districts. Nearly 30% of the road finance goes for road development in these districts. This project is mainly carried out to focus mainly on rural road network connectivity.</li>
	<li class="gap-above-small">Postal Code mapping with region and limits of Post office for several North Karnataka districts.</li>
	<li class="gap-above-small">Technical support to district planning in resource mapping using high resolution satellite imageries, GPS etc., specifically in Gulbarga, Dakshina Kannada, Gadag, Tumkur and Chitradurga.</li>
	<li class="gap-above-small">NRDMS centres are providing analysed spatial information to support district planning activities mainly in the areas of education, health, infrastructure, watersheds based on local specific problems.</li>
	<li class="gap-above-small">Preparation of safer zone for Stone crushing in several districts of the state for Mines and Geology department based on the guidelines provided by the district authorities.</li>
	<li class="gap-above-small">Water quality and quantity analysis for the State has been taken up and the Council has completed the generation of spatial maps for few selected districts under Jal Nirmal program.</li>
	<li class="gap-above-small">Digital Tourism ATLAS for the state of Karnataka showing tourist information like religious place / water falls / hill stations / adventure sport / national parks / bird sanctuary / monuments / forts / reservoirs etc., has been completed.</li>
	<li class="gap-above-small">NRDMS centres are providing analysed spatial information to support district planning activities mainly in the areas of education, health, infrastructure, watersheds based on local specific problems etc.</li>
</ul>
