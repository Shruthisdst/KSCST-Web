<h3>Background</h3>
<p>Motivated by the desire to build a strong base for practical problem solving in several engineering colleges as well as to build their R &amp; D potential, KSCST decided to promote student project programmes in a large number of educational institutions, located Karnataka, in areas of engineering, medicine, fisheries and agriculture. It saw an enormous reservoir of talent and creativity in the students and the faculty of engineering colleges in Karnataka. Another important factor is the abundant availability of basic and infrastructure facilities in the engineering colleges for experimentation, design, fabrication and testing.</p>
<p><img class="img-border" src="<?=PUBLIC_URL?>images/stock/spp_logo.jpg" alt="Student Project Programme"></p>
<p>KSCST, therefore, ventured into launching and implementing a programme called STUDENT PROJECT PROGRAMME (SPP) for providing financial and academic support for Bachelor of Engineering projects. Discussions held with the Director of Technical Education and the Principals of engineering colleges indicated that such a programme would be worthwhile.</p>
<p>Started in 1977-78, this programme is a unique experiment in Karnataka and it is a tribute to the foresight and inspired through on the part of a group of dedicated scientists, engineers and executives and administrators of Government of Karnataka. This programme is also a major innovation and first of its kind in technical education in the country and has a major impact in improving the quality of technical education. It links together developmental efforts with educational institutions so that relevant problems flow to the institutions and hopefully feasible technical solutions might flow back to the implementers.</p>
<p>The student project programme is now 37 years old. From 1977-78 to 2013-2014 about 7000 projects at a total cost of more than 2.5 Crore Rupees have been funded to various engineering colleges.</p>
<h3>Objectives</h3>
<ul>
	<li class="gap-above-small">Creativity of students applied to solve development problems of our people and State through Science and Technology.</li>
	<li class="gap-above-small">Enrich Collegiate education through finding solutions to real life problems.</li>
	<li class="gap-above-small">Improve understanding and develop methodology of solving complex issues.</li>
</ul>
<h3>Issues Focused</h3>
<ul>
	<li class="gap-above-small">Several studies focused on rural issues have helped in development activities in Karnataka. Dissemination of technology pertaining to low cost housing technology, ground water study, low cost agricultural implements, assessment of drinking water potential, in specific areas have been notable examples.</li>
	<li class="gap-above-small">This programme has enriched collegiate education through finding solutions to real life problems. This has also improved the understanding and development of a methodology to solve complex issues.</li>
	<li class="gap-above-small">This programme has utilized the services of experts from various scientific and technical institutions like Indian Institute of Science, Indian Institute of Management, National Aerospace Laboratory and in addition to local organizations like Village Panchayats, Block Development Boards, Municipalities, Zilla Panchayats, etc.</li>
</ul>
<p>Projects sponsored by KSCST through this programme have been recognized by many industries and helped the students to seek employment. So far, this programme has utilized the tremendous talent and potential of the students and the rich experience of engineering faculty for tackling the real life problems of people in Karnataka</p>
<h3>Methodology</h3>
<p>Each proposal is reviewed by an expert and only those recommended are supported by the KSCST (Approximately one project proposal out of three gets funded) More than one hundred senior faculty of Indian Institute of Science, scientists in national labs and other experts review the proposals. They also provide guidelines and suggestions to make a project innovative and meaningful. A modest financial support is also provided.</p>
<p>On the completion of the project, a team of experts examines the project at a nodal centre and selects the better ones for the annual Seminar and Exhibition. It helps to identify motivated faculty from the respective academic institutions and initiate interactions that could lead to even better projects.</p>
<p>An annual Seminar and Exhibition of selected student projects (about 150 projects selected by the evaluation team of experts) is held every year in one of the engineering colleges wherein all the Principals, faculty members and students of engineering colleges and some experts from all over the country are invited to participate. Awards for the best projects and project guides are also given during the seminar and exhibition.</p>
<h3>Progress in Thirty Seven Years</h3>
<p>Born in 1977, the Student Project Programme is now 36 years old. It started with 54 projects in 13 engineering colleges at a total funding of Rs. 1.2 lakhs i.e., about Rs. 2,250/- per project on an average. During the 37th series (2013-2014) of this programme which was commenced in November 2013, a total of 370 projects in 111 colleges were sponsored by the Council at a total funding of over Rs. 18.80 lakhs.</p>
<p>From 1977-78 to 2013-2014 about 7000 projects at a total cost of nearly 2.5 Crore Rupees have been funded to engineering colleges and to a few polytechnics. Out of these, approximately 3,100 projects (45% of total projects) were selected for annual Seminar and Exhibition held in various engineering colleges during these 37 years.</p>
<p>In addition to engineering colleges and polytechnics, this programme was extended to agriculture, fisheries, veterinary, medical and other general topics relevant to the developmental needs of Karnataka.</p>
<h3>Seminar and Exhibition</h3>
<p>So far 37 Seminar and Exhibitions have been organized in different engineering colleges. An expert panel is constituted to select best projects for the PROJECT OF THE YEAR award. A trophy is also presented every year to the Best Performance College along with cash prizes. In order to encourage all the students of the selected projects for Seminar and Exhibition, each one of them will be given a certificate and a prize in the form a technical book.</p>
<p>The 37th Series SPP Seminar and Exhibition was organised at B. V. Bhoomaraddi College of Engineering and Technology, Hubli between 28 - 29th July 2014.</p>
