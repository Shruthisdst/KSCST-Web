<p class="gap-above"><strong>III. The procedure followed in the decision making process, including channels of supervision and accountability.</strong></p>
<p>The General Body of the State Council is the supreme body headed by the President and followed by three Vice-Presidents and members. The General Body of the Council will review its activities; provide broader frame work and guidelines of its future course of action, which is the responsibility of the Executive Committee for implementation of the same.</p>
<p>The administration and management of the affairs and finances of the State Council are conducted by the Executive Committee, which is responsible for smooth functioning of the activities in pursuance of State Council's objectives. Executive Committee shall have the power to make bye-laws not inconsistent with its rules for conduct of the administrative, financial and other affairs of the State Council in furtherance of its objectives and also to amend its rules as and when necessary. It is the responsibility of the Member-Secretary, Executive Committee to implement the decisions taken by the Executive Committee.</p>
<p>The Member-Secretary, Executive Committee, who is the Principal Executive Officer, is assisted by full-time Officers namely, the Executive Secretary, Administrative Officer and Accounts Officer who are assisted by middle level officers of Scientists, Senior Fellow, Fellows, Assistant Accounts Officers and Assistant Administrative Officer. These officers are assisted by secretarial staff. The organization chart of the Council is enclosed.</p>
