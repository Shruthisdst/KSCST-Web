<p class="gap-above"><strong>I. The particulars of its organization, functions and duties.</strong></p>
<ol>
	<li class="gap-above-small">Karnataka State Council for Science and Technology is an autonomous body and registered as a Society under the Karnataka Societies Registration Act, 1960.</li>
	<li class="gap-above-small">The functions of the Council are:
		<ol>
			<li>To identify areas for the application of Science and Technology to the development needs, objectives and goals of Karnataka, and in particular, to the prevailing conditions of backwardness, rural unemployment and poverty.</li>
			<li>To advise Government on the formulation of policies and measures, including technical, administrative and legal devices, which will promote such application to identified needs, objectives and goals in particular to health, education and manpower utilization with special emphasis on the development of human skills in the rural areas and in the slums and which will promote the scientific management of the natural resources of the State.</li>
			<li>To promote effective co-ordination and to develop and faster communication and other links between centres of scientific and technological research, Government agencies, farms and industries so that promising Research &amp; Development work is initiated, promoted and effectively deployed in agriculture, in industry, in Government and elsewhere.</li>
			<li>To initiate, support and co-ordinate applied research programmes in Universities and other institutions in areas identified to be specially suitable for the application of Science and Technology.</li>
			<li>To prepare Science and Technology plans relevant to the development needs of the State.</li>
			<li>To consider and advise Government on such other matters as are relevant to the application of Science and Technology to the problems of Karnataka State.</li>
		</ol>
	</li>
</ol>
